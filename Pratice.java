public class Pratice{
	public static void main(String[] args){
		// Java escape characters
		// Single quote(\''): this allows single quote
		System.out.println("Update to a \'beam' of light whatsapp");
		// Double quote(\""): this allows double quote
		System.out.println("Update to a \"beam\" of light whatsapp");
		// Backslash(\\): this allows a backslash to be added
		System.out.println("Yes \\ No");
		// Tab(\t): it is used to insert a tab in the text
		System.out.println("look at the \tspace");
		// Carriage return(\r): it is used to display text at the right
		System.out.println("this will not display, \rbut this will");
		// New line(\n): like python this is used to insert a new line
		System.out.println("Old line, \nNew line");
		
		float x=(3 * 9 * (3 + (9 * 3 / (3)))); //324
		System.out.print(x);
		
	}
}
