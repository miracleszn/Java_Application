public class LogicalOperators{
	public static void main(String[] args){
		int x = 50;
		int y = 25;
		int z = 15;
		
		boolean result = (x > y) && (x <= z);
		System.out.println(result);
		
		boolean result = (x > y) && (x <= z);
		System.out.println(result);
		
		boolean result = (x > y) && (x <= z);
		System.out.println(result);
	
	}
}